CLI_COMMAND_RE = re.compile(r"`(ovos-[a-z0-9\-]+)")

# Commands that either launch a long-running daemon/service or open an
# interactive session connected to a *live* messagebus. Their `--help` never
# returns on its own (or worse, actually starts the service) so they are
# deliberately excluded from --help capture, per the "never start long-running
# daemons" ground rule. They stay documented only via the hand-written tables.
UNSAFE_FOR_HELP_CAPTURE = {
    "ovos-core", "ovos-messagebus", "ovos-dinkum-listener", "ovos-audio",
    "ovos-gui-service", "ovos-gui-debug-tui", "ovos-intent-service",
    "ovos-skill-installer", "ovos-ocp-standalone",
    "ovos-listen", "ovos-speak", "ovos-say-to", "ovos-simple-cli",
}


def _capture_help(cmd: str, venv_bin: Path) -> str | None:
    exe = venv_bin / cmd
    if not exe.exists():
        which = subprocess.run(["which", cmd], capture_output=True, text=True)
        if which.returncode != 0:
            return None
        exe = Path(which.stdout.strip())
    try:
        result = subprocess.run(
            [str(exe), "--help"], capture_output=True, text=True, timeout=15,
        )
        out = (result.stdout or "") + (result.stderr or "")
        return out.strip()
    except Exception as exc:  # pragma: no cover - best effort
        return f"(--help failed: {exc})"


def gen_cli(venv_bin: str | None) -> None:
    venv = Path(venv_bin or (Path.home() / ".venvs/ovos/bin"))
    page = DOCS / "cli-tools.md"
    text = page.read_text()
    commands = sorted(set(CLI_COMMAND_RE.findall(text)))

    blocks = []
    skipped = []
    excluded = []
    for cmd in commands:
        if cmd in UNSAFE_FOR_HELP_CAPTURE:
            excluded.append(cmd)
            continue
        help_text = _capture_help(cmd, venv)
        if help_text is None:
            skipped.append(cmd)
            continue
        blocks.append(f"??? example \"`{cmd} --help`\"\n\n    ```text\n" + "\n".join(
            "    " + line for line in help_text.splitlines()
        ) + "\n    ```")

    body = "\n\n".join(blocks) if blocks else "*(no installed commands captured)*"
    page_text = text
    new_text, found = replace_marked_section(page_text, "cli-help-output", body)
    if not found:
        marker_block = wrap("cli-help-output", body)
        anchor = "\n---\n\n*Source code:"
        if anchor in page_text:
            new_text = page_text.replace(
                anchor,
                f"\n---\n\n## Command `--help` Output (Generated)\n\n{marker_block}\n{anchor}",
                1,
            )
        else:
            new_text = page_text.rstrip() + f"\n\n## Command `--help` Output (Generated)\n\n{marker_block}\n"
    page.write_text(new_text)
    print(f"[cli] captured --help for {len(blocks)}/{len(commands)} documented commands in {page.relative_to(REPO_ROOT)}")
    if skipped:
        print(f"[cli] not installed, skipped: {skipped}")
    if excluded:
        print(f"[cli] excluded (daemon/live-bus command, unsafe to invoke): {excluded}")


# ---------------------------------------------------------------------------

def main() -> None:
    ap = argparse.ArgumentParser(description=__doc__, formatter_class=argparse.RawDescriptionHelpFormatter)
    sub = ap.add_subparsers(dest="cmd", required=True)

    p_wf = sub.add_parser("workflows", help="regenerate gh-automations-workflows.md")
    p_wf.add_argument("--repo-path", default=None, help="local gh-automations clone (default: ~/AgentWorkspaces/ovos/gh-automations)")

    sub.add_parser("config", help="regenerate config-reference.md")

    p_cli = sub.add_parser("cli", help="regenerate cli-tools.md")
    p_cli.add_argument("--venv-bin", default=None, help="venv bin/ dir to look up commands (default: ~/.venvs/ovos/bin)")

    p_all = sub.add_parser("all", help="run all generators")
    p_all.add_argument("--repo-path", default=None)
    p_all.add_argument("--venv-bin", default=None)

    args = ap.parse_args()

    if args.cmd == "workflows":
        gen_workflows(args.repo_path)
    elif args.cmd == "config":
        gen_config()
    elif args.cmd == "cli":
        gen_cli(args.venv_bin)
    elif args.cmd == "all":
        gen_workflows(args.repo_path)
        gen_config()
        gen_cli(args.venv_bin)


if __name__ == "__main__":
    main()
