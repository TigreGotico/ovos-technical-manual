def _strip_jsonc_comments_with_map(text: str):
    """Return (stripped_json_text, {dotted.key.path: description}).

    Walks mycroft.conf line by line, tracking nesting via brace/bracket
    counting on the *stripped* content so we can associate each `//` comment
    block with the key it immediately precedes.
    """
    lines = text.splitlines()
    descriptions: dict[str, str] = {}
    path_stack: list[str] = []
    pending_comment: list[str] = []
    out_lines = []
    key_re = re.compile(r'^\s*"([^"]+)"\s*:\s*(.*)$')

    for raw in lines:
        stripped = raw.strip()
        if stripped.startswith("//"):
            pending_comment.append(stripped.lstrip("/").strip())
            continue
        # strip trailing // comments (best effort, ignores // inside strings)
        line_no_comment = re.sub(r'(?<!:)//.*$', '', raw) if "://" not in raw else raw
        out_lines.append(line_no_comment)

        m = key_re.match(stripped)
        if m and pending_comment:
            key = m.group(1)
            dotted = ".".join(path_stack + [key])
            descriptions[dotted] = " ".join(pending_comment)
        if m:
            rest = m.group(2)
            if rest.startswith("{"):
                path_stack.append(m.group(1))
        elif stripped.startswith("{") and path_stack:
            pass
        if stripped in ("},", "}"):
            if path_stack:
                path_stack.pop()
        pending_comment = []

    stripped_json = "\n".join(out_lines)
    # remove trailing commas which plain json can't handle
    stripped_json = re.sub(r",\s*([}\]])", r"\1", stripped_json)
    return stripped_json, descriptions


def _flatten(d, prefix=""):
    items = []
    for k, v in d.items():
        key = f"{prefix}.{k}" if prefix else k
        if isinstance(v, dict) and v:
            items.extend(_flatten(v, key))
        else:
            items.append((key, v))
    return items


def _fmt_json_value(v) -> str:
    import json
    if isinstance(v, str):
        return f'`"{v}"`' if v else '`""`'
    if isinstance(v, list):
        return "`[]`" if not v else f"`{json.dumps(v)}`"
    if isinstance(v, dict):
        return "`{}`" if not v else "*(object, see below)*"
    if isinstance(v, bool):
        return f"`{str(v).lower()}`"
    return f"`{json.dumps(v)}`"


def gen_config() -> None:
    try:
        from ovos_config.locations import DEFAULT_CONFIG  # type: ignore
    except ImportError:
        sys.exit("ovos-config is not importable in this interpreter; activate ~/.venvs/ovos")

    raw = Path(DEFAULT_CONFIG).read_text()
    stripped, comment_map = _strip_jsonc_comments_with_map(raw)

    try:
        from ovos_utils.json_helper import load_commented_json  # type: ignore
        data = load_commented_json(DEFAULT_CONFIG)
    except Exception:
        import json
        data = json.loads(stripped)

    overrides_path = REPO_ROOT / "tools" / "config_descriptions.yaml"
    overrides = {}
    if overrides_path.exists():
        overrides = yaml.safe_load(overrides_path.read_text()) or {}

    flat = _flatten(data)
    lines = ["| Key | Default | Description |", "|---|---|---|"]
    for key, value in flat:
        desc = overrides.get(key) or comment_map.get(key, "")
        lines.append(f"| `{key}` | {_fmt_json_value(value)} | {_md_escape(desc)} |")
    body = "\n".join(lines)

    page = DOCS / "config-reference.md"
    text = page.read_text()
    new_text, found = replace_marked_section(text, "mycroft-conf-keys", body)
    if not found:
        # First run: append a new marked section right before the trailing
        # "Source code:" line so existing hand-written sections are untouched.
        marker_block = wrap("mycroft-conf-keys", body)
        anchor = "\n---\n\n*Source code:"
        if anchor in text:
            new_text = text.replace(anchor, f"\n---\n\n## All Keys (Generated)\n\n{marker_block}\n{anchor}", 1)
        else:
            new_text = text.rstrip() + f"\n\n## All Keys (Generated)\n\n{marker_block}\n"
    page.write_text(new_text)
    print(f"[config] regenerated mycroft.conf key table ({len(flat)} keys) in {page.relative_to(REPO_ROOT)}")


