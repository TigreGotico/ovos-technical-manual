#!/usr/bin/env python3
"""
Regenerate the drift-prone reference pages of the OVOS technical manual from
their sources of truth: gh-automations reusable workflows, ovos-config's
bundled mycroft.conf, and installed OVOS console scripts.

Only the content between matching
    <!-- BEGIN GENERATED: <id> -->
    <!-- END GENERATED -->
marker pairs is touched. Everything else in the target pages (intros, usage
examples, behavior notes, jobs tables, etc.) is hand-written prose and is
left untouched.

Usage:
    python3 tools/gen_reference.py workflows [--repo-path PATH]
    python3 tools/gen_reference.py config
    python3 tools/gen_reference.py cli [--venv-bin PATH]
    python3 tools/gen_reference.py all

Requires: PyYAML (`pip install pyyaml`), run with any Python that has it
(the project's docs venv works fine).
"""
from __future__ import annotations

import argparse
import re
import subprocess
import sys
from pathlib import Path

import yaml

REPO_ROOT = Path(__file__).resolve().parent.parent
DOCS = REPO_ROOT / "docs"

GEN_BEGIN = "<!-- BEGIN GENERATED: {id} -->"
GEN_END = "<!-- END GENERATED -->"
GEN_NOTE = (
    "<!-- This section is auto-generated by tools/gen_reference.py from the "
    "upstream source of truth. Do not hand-edit; re-run the script instead. -->"
)


def wrap(section_id: str, body: str) -> str:
    return f"{GEN_BEGIN.format(id=section_id)}\n{GEN_NOTE}\n{body.rstrip()}\n{GEN_END}"


def replace_marked_section(text: str, section_id: str, new_body: str) -> tuple[str, bool]:
    """Replace the content between BEGIN/END markers for `section_id`.

    Returns (new_text, found). If the markers don't exist yet, returns the
    text unchanged with found=False so the caller can insert them.
    """
    begin = re.escape(GEN_BEGIN.format(id=section_id))
    pattern = re.compile(begin + r".*?" + re.escape(GEN_END), re.DOTALL)
    replacement = wrap(section_id, new_body)
    if pattern.search(text):
        return pattern.sub(lambda _m: replacement, text, count=1), True
    return text, False


# ---------------------------------------------------------------------------
# a) gh-automations reusable workflow reference
# ---------------------------------------------------------------------------

def _md_escape(val) -> str:
    if val is None:
        return ""
    s = str(val)
    return s.replace("|", "\\|").replace("\n", " ").strip()


def _fmt_default(inp: dict) -> str:
    if "default" not in inp:
        return "_(required)_" if inp.get("required") else ""
    d = inp["default"]
    if isinstance(d, bool):
        d = str(d).lower()
    if d == "":
        return '`""`'
    return f"`{d}`"


SECRET_USE_RE = re.compile(r"secrets\.([A-Z][A-Z0-9_]*)\b")


def find_undeclared_secrets(raw_yaml_text: str, declared: dict) -> dict:
    """Some workflows reference `secrets.X` without a formal workflow_call.secrets
    declaration (relying on `secrets: inherit` from the caller). Surface those
    too, so the doc doesn't silently lose them."""
    found = {}
    for name in sorted(set(SECRET_USE_RE.findall(raw_yaml_text))):
        if name in ("GITHUB_TOKEN",) or name in declared:
            continue
        found[name] = {"description": "(used via `secrets: inherit`; not formally declared as a workflow_call secret)"}
    return found


def render_workflow_tables(wc: dict, extra_secrets: dict | None = None) -> str:
    parts = []
    inputs = wc.get("inputs") or {}
    if inputs:
        lines = ["### Inputs", "", "| Input | Type | Default | Description |", "|-------|------|---------|-------------|"]
        for name, spec in inputs.items():
            spec = spec or {}
            lines.append(
                f"| `{name}` | {spec.get('type', 'string')} | {_fmt_default(spec)} | {_md_escape(spec.get('description'))} |"
            )
        parts.append("\n".join(lines))

    secrets = dict(wc.get("secrets") or {})
    secrets.update(extra_secrets or {})
    if secrets:
        lines = ["### Secrets", "", "| Secret | Required | Description |", "|--------|----------|-------------|"]
        for name, spec in secrets.items():
            spec = spec or {}
            req = "yes" if spec.get("required") else "no"
            lines.append(f"| `{name}` | {req} | {_md_escape(spec.get('description'))} |")
        parts.append("\n".join(lines))

    outputs = wc.get("outputs") or {}
    if outputs:
        lines = ["### Outputs", "", "| Output | Description |", "|--------|-------------|"]
        for name, spec in outputs.items():
            spec = spec or {}
            lines.append(f"| `{name}` | {_md_escape(spec.get('description'))} |")
        parts.append("\n".join(lines))

    return "\n\n".join(parts)


def load_workflow_call(path: Path) -> dict | None:
    doc = yaml.safe_load(path.read_text())
    on_key = True if True in doc else "on"
    on = doc.get(on_key)
    if not isinstance(on, dict):
        return None
    return on.get("workflow_call")


WORKFLOW_SECTION_RE = re.compile(
    r"(^## `(?P<name>[\w.\-]+\.yml)`.*?\*\*Source:\*\* `\.github/workflows/(?P=name)`\n\n)"
    r"(?P<body>.*?)"
    r"(?=\n### (?!Inputs|Secrets|Outputs)|\n---\n|\Z)",
    re.DOTALL | re.MULTILINE,
)


def gen_workflows(repo_path: str | None) -> None:
    repo = Path(repo_path or Path.home() / "AgentWorkspaces/ovos/gh-automations")
    if not (repo / ".git").exists():
        sys.exit(f"gh-automations clone not found at {repo} (pass --repo-path)")
    subprocess.run(["git", "fetch", "origin", "dev"], cwd=repo, capture_output=True)
    # Prefer the freshly fetched origin/dev; fall back to whatever ref is
    # already checked out (e.g. a CI checkout pinned to ref: dev) so this
    # also works offline / in a shallow CI clone without a remote named origin.
    ref = "origin/dev"
    if subprocess.run(["git", "rev-parse", "--verify", "-q", ref], cwd=repo, capture_output=True).returncode != 0:
        ref = "HEAD"
    # Use the local worktree content updated to origin/dev without mutating the
    # user's checkout: read blobs directly from the fetched ref.
    ls = subprocess.run(
        ["git", "ls-tree", "-r", "--name-only", ref, ".github/workflows"],
        cwd=repo, check=True, capture_output=True, text=True,
    ).stdout.splitlines()

    workflow_calls: dict[str, dict] = {}
    raw_contents: dict[str, str] = {}
    for relpath in ls:
        if not relpath.endswith(".yml"):
            continue
        name = Path(relpath).name
        content = subprocess.run(
            ["git", "show", f"{ref}:{relpath}"], cwd=repo, check=True, capture_output=True, text=True,
        ).stdout
        raw_contents[name] = content
        doc = yaml.safe_load(content)
        on_key = True if True in doc else "on"
        on = doc.get(on_key)
        if not isinstance(on, dict):
            continue
        wc = on.get("workflow_call")
        if wc:
            workflow_calls[name] = wc

    page = DOCS / "gh-automations-workflows.md"
    text = page.read_text()

    def _sub(m: re.Match) -> str:
        name = m.group("name")
        wc = workflow_calls.get(name)
        if not wc:
            return m.group(0)  # leave untouched if source workflow not found
        extra_secrets = find_undeclared_secrets(raw_contents.get(name, ""), wc.get("secrets") or {})
        body = render_workflow_tables(wc, extra_secrets)
        return m.group(1) + wrap(f"workflow-io:{name}", body) + "\n"

    new_text, n = WORKFLOW_SECTION_RE.subn(_sub, text)
    page.write_text(new_text)
    documented = {m.group("name") for m in WORKFLOW_SECTION_RE.finditer(text)}
    missing = documented - set(workflow_calls)
    extra = set(workflow_calls) - documented
    print(f"[workflows] regenerated {n} workflow section(s) in {page.relative_to(REPO_ROOT)}")
    if missing:
        print(f"[workflows] WARNING: documented but not found in source: {sorted(missing)}")
    if extra:
        print(f"[workflows] NOTE: reusable workflows in source but undocumented: {sorted(extra)}")


